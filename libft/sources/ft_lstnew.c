#include "libft.h"
#include <stdio.h>

t_list	*ft_lstnew(void *content)
{
	t_list	*new;

	new = ft_calloc(1, sizeof(t_list));
	if (new == NULL)
		return (0);
	new -> content = content;
	new -> index = 0;
	new -> next = NULL;
	new -> prev = NULL;
	return (new);
}
